# ── 비도서 필터 패턴 ──────────────────────────────────────────────────────────
NON_BOOK_PATTERNS = [
    r'안내$', r'공고$', r'알림$', r'신청$', r'모집$', r'채용$',
    r'워크숍', r'행사', r'세미나', r'기념식', r'시상식', r'전시',
    r'서비스 (중단|변경|오류)', r'설문조사', r'아르바이트',
    r'공개 모집', r'참석 대상', r'기간 연장',
]
NON_BOOK_RE = re.compile('|'.join(NON_BOOK_PATTERNS))

def is_real_book(book):
    title = book.get('title', '')
    desc  = book.get('description', '')
    # 설명이 없고 제목이 공지성이면 제거
    if not desc and NON_BOOK_RE.search(title):
        return False
    # 설명이 30자 이상이거나 author가 사람이름이면 도서
    if len(desc) >= 30:
        return True
    if len(title) >= 4 and not NON_BOOK_RE.search(title):
        return True
    return False

