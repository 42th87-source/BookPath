def classify_kdc(title, desc):
    text = title + " " + desc
    scores = {kdc: 0 for kdc in KDC_RULES}
    for kdc, weight_dict in KDC_RULES.items():
        for weight, keywords in weight_dict.items():
            for kw in keywords:
                if kw in text:
                    scores[kdc] += weight
    best_kdc, best_score = max(scores.items(), key=lambda x: x[1])
    return best_kdc if best_score > 0 else "800"  # 기본값: 문학

