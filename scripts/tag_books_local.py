    pool = KDC_CAREERS_CURATED.get(kdc, ["연구원", "교사", "전문가"])
    scored = []
    for career in pool:
        keywords = CAREER_SIGNAL_KEYWORDS.get(career, [])
        # 제목 매칭 5점, 설명 매칭 1점 — 제목에 분야명이 있으면 압도적 우선
        score = sum(5 for kw in keywords if kw in title) + \
                sum(1 for kw in keywords if kw in desc)
        scored.append((score, career))
    scored.sort(key=lambda x: -x[0])
    return [c for _, c in scored[:3]]

