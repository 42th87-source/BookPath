def extract_desc_keywords(desc, max_kw=3):
    if not desc:
        return []
    # 2~6글자 한글 단어 추출
    words = re.findall(r'[가-힣]{2,6}', desc)
    word_freq = {}
    for w in words:
        if w not in DESC_STOPWORDS:
            word_freq[w] = word_freq.get(w, 0) + 1
    # 빈도 2회 이상이고 고유한 단어 우선
    meaningful = [w for w, c in sorted(word_freq.items(), key=lambda x: -x[1])
                  if c >= 2 and len(w) >= 2]
    return meaningful[:max_kw]

def build_student_tags(title, desc, kdc):
    pool = KDC_TAG_POOL.get(kdc, [])
    tags = []

    # 1) 제목·설명과 관련성 높은 풀 태그 먼저 선택
    for t in pool:
        # 태그 내 핵심 단어 중 하나가 제목이나 설명에 포함되면 우선 선택
        core = t.replace('탐구', '').replace('학습', '').replace('이해', '').strip()
        if core and (core in title or core in desc):
            tags.append(t)
        if len(tags) >= 2:
            break

    # 2) 부족하면 풀 순서대로 채우기
    for t in pool:
        if t not in tags:
            tags.append(t)
        if len(tags) >= 4:
            break

    return tags[:4]

